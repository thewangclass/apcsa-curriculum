public class EscapeSequences
{
    public static void main(String[] args){
        System.out.println("beep beep\nboop boop");
        System.out.println("beep beep bo\nop boop");
        System.out.println("beep\"beep boop boop");
        System.out.println("beep\"beep bo/op boop");
        System.out.println("beep\'beep bo/op boop");
        System.out.println("b\teep bo\nop bo\"op");

    }
}
