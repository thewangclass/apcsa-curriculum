public class AssigningValues
{
    public static void main(String[] args){
        int blah; // declare blah as an int variable
        blah = 40;
        boolean yes; // declare yes as boolean
        yes = false;
        double decNumber;
        decNumber = 55.7;

        
        /* 
         * Or we can declare and assign at the same time
         * int blah = 40;
         * boolean yes = false;
         * double decNumber = 55.7;
         */
    }
}
